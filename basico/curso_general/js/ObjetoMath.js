console.log("Objeto Math");

//Obtener PI
console.log(Math.PI);

//Mostrar solo 2 decimales
console.log(Math.PI.toFixed(2));

//Quitar los decimales
console.log(Math.floor(Math.PI));

//Redondear un número
console.log(Math.round(4.6));
console.log(Math.round(Math.PI));

//Pasar un número negativo a su valor absoluto positivo
console.log(Math.abs(-451));

//Calcular la potencia (base, exponente)
console.log(Math.pow(2, 3));
console.log(2 ** 3);

//Crear un número aleatorio entre 0 y 1 (no incluye el 1)
console.log(Math.random());

//Crear un número positivo entre 0 y 10.algo
console.log(Math.random() * 10 + 1);

//Crear un número positivo entre 0 y 10, sin deicmales
console.log(Math.floor(Math.random() * 10 + 1));